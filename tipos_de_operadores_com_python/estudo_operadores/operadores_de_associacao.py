"""
    operadores de associação indicam se
    o elemento encontra-se em um iterável
"""

frutas = ['limão', 'uva']
print('laranja' in frutas) # laranja está em frutas?
print('limão' not in frutas) # limão não está em frutas?

# Lembrete: Python é 'case sensitive'.
curso = 'curso de python'
print('python' in curso) #" 'python' está em curso?"
print('Python' in curso) #" 'Python' está em curso? "